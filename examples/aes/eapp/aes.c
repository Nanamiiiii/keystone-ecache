#include <stdio.h>
#include <string.h>
#include <time.h>
#include "aes.h"

#define TEXT_LENGTH 32

static const uint8_t key[] ={ 0x60, 0x3d, 0xeb, 0x10, 0x15, 0xca, 0x71, 0xbe, 0x2b, 0x73, 0xae, 0xf0, 0x85, 0x7d, 0x77, 0x81,
                      						0x1f, 0x35, 0x2c, 0x07, 0x3b, 0x61, 0x08, 0xd7, 0x2d, 0x98, 0x10, 0xa3, 0x09, 0x14, 0xdf, 0xf4 };
static const uint8_t iv[]  = { 0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f };

int main(void){
	printf("start enclave\n");
	clock_t start, end;
	struct AES_ctx ctx;
	static unsigned char text[TEXT_LENGTH] = {
    0x0f, 0xaa, 0xd4, 0xff, 0x01, 0x17, 0x85, 0x83, 0xba, 0xa5, 0x88,
	0x96, 0x6f, 0x7c, 0x1f, 0xf3, 0x25, 0x64, 0xdd, 0x17, 0xd7, 0xdc,
	0x2b, 0x46, 0xcb, 0x50, 0xa8, 0x4a, 0x69, 0x27, 0x0b, 0x4c};
	start = clock();
	AES_init_ctx_iv(&ctx, key, iv);
	AES_CBC_encrypt_buffer(&ctx, (uint8_t*)text, TEXT_LENGTH);
	printf("暗号化後のデータ: ");
	for (int i = 0; i < TEXT_LENGTH; i++) {
		printf("%02x", text[i]);
	}

	AES_init_ctx_iv(&ctx, key, iv);
    AES_CBC_decrypt_buffer(&ctx, (uint8_t*)text, TEXT_LENGTH);
	end =clock();
	printf("復元したデータ: ");
	for (int i = 0; i < TEXT_LENGTH; i++) {
		printf("%02x", text[i]);
	}
	printf("CBC decrypt: %s", text);
	
	printf("\n");
	printf("time = %ld\n", ((end-start)/CLOCKS_PER_SEC));
  return 0;
}